package com.fiveplusone.arraydemo;

public class ArrayDemo4 {
    static void main(String[] args) {
        String[] arr = new String[100];
        arr[0] = "ZhangSan";
        arr[1] = "LiSi";
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
        //默认初始值：
        //整数0
        //小数0.0
        //布尔值false
        //字符'\u0000'
        //引用类型null
    }
}
